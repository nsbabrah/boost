	this.setCanvasSize();
	this.ctx.clearRect(0, 0, this.w, this.h);
	this.ctx.font = "Bold 28px sans-serif";
		// this.ctx.font-weight= "bold";
	this.ctx.font="margin: 0 20px 20px";
